class starpattern {

    public static void main(String args[]) {

        int a = 1;
        int i;
        for (i = 0; i < 3; i++) {
            System.out.print(" ");
        }

        for (int j = 0; j <= 3; j++) {
            System.out.print(a + " ");
            a++;
        }
        System.out.println(" ");

    }
}